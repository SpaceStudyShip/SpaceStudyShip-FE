  /// 특정 행성의 지역 목록 반환
  static List<ExplorationNodeEntity> getRegions(String planetId) {
    return regions[planetId] ?? [];
  }

  /// 특정 행성 반환
  static ExplorationNodeEntity getPlanet(String planetId) {
    return planets.firstWhere(
      (p) => p.id == planetId,
      orElse: () => planets.first,
    );
  }
